package day04;

public class SwitchDemo {
	public static void main(String[] args) {
		int num = 4;
		switch(num) {
		case 1:
			System.out.println(111);
			break;
		case 2:
			System.out.println(222);
			break;
		case 3:
			System.out.println(333);
			break;
		default:
			System.out.println(666);
		}
	}
}
